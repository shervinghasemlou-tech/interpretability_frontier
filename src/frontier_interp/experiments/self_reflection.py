"""Self-reflection experiments.

These experiments ask whether the frozen target model can access its own
behavior and mechanisms via:
1. attached self-probes on its hidden states,
2. prompted self-reports generated by the model itself.
"""

from __future__ import annotations

import re
import time
from typing import Dict, List, Tuple

import numpy as np
import torch
import torch.nn.functional as F

from frontier_interp.experiments.mechanism import _attention_training_loss
from frontier_interp.utils.time import format_seconds


def train_self_probe_behavior(probe, target_model, train_batches, *, steps: int, logger, run_label: str, lr: float, weight_decay: float):
    opt = torch.optim.AdamW(probe.parameters(), lr=lr, weight_decay=weight_decay)
    probe.train()
    batches = list(train_batches)
    baseline_loss = None
    start = time.time()

    for step in range(steps):
        batch = batches[step % len(batches)]
        token_batch = target_model.tokenize_batch([ex.text for ex in batch['examples']], batch['max_prompt_len'])
        with torch.no_grad():
            target_logits, _, hidden_states = target_model.extract_logits_attentions_hidden(token_batch)
            hidden = hidden_states[-1]

        pred_logits = probe.forward_behavior_from_hidden(hidden)
        loss = F.kl_div(F.log_softmax(pred_logits, dim=-1), F.softmax(target_logits, dim=-1), reduction='batchmean')

        if baseline_loss is None:
            uniform_logits = torch.zeros_like(pred_logits)
            baseline_loss = F.kl_div(F.log_softmax(uniform_logits, dim=-1), F.softmax(target_logits, dim=-1), reduction='batchmean').item()
            baseline_loss = max(baseline_loss, 1e-8)

        opt.zero_grad()
        loss.backward()
        opt.step()

        if step % 100 == 0 or step == steps - 1:
            elapsed = time.time() - start
            done = step + 1
            eta = (steps - done) * (elapsed / max(done, 1))
            logger.log(f"{run_label}[self-probe-behavior] step={step:4d}/{steps} | loss={loss.item():.4f} | normed={loss.item()/baseline_loss:.4f} | elapsed={format_seconds(elapsed)} | eta={format_seconds(eta)}")

    return baseline_loss


@torch.no_grad()
def eval_self_probe_behavior(probe, target_model, val_batches, baseline_loss: float):
    probe.eval()
    rows = []
    for batch in val_batches:
        token_batch = target_model.tokenize_batch([ex.text for ex in batch['examples']], batch['max_prompt_len'])
        target_logits, _, hidden_states = target_model.extract_logits_attentions_hidden(token_batch)
        hidden = hidden_states[-1]
        pred_logits = probe.forward_behavior_from_hidden(hidden)
        loss = F.kl_div(F.log_softmax(pred_logits, dim=-1), F.softmax(target_logits, dim=-1), reduction='batchmean').item()
        top1 = (pred_logits.argmax(dim=-1) == target_logits.argmax(dim=-1)).float().mean().item()
        rows.append({
            'family': batch['family'],
            'source': batch['source'],
            'normed_loss': loss / baseline_loss,
            'top1_match': top1,
        })
    probe.train()
    return rows


def train_self_probe_mechanism(probe, target_model, train_batches, *, layer_idx: int, head_idx: int, mechanism_type: str, steps: int, logger, run_label: str, lr: float, weight_decay: float):
    opt = torch.optim.AdamW(probe.parameters(), lr=lr, weight_decay=weight_decay)
    probe.train()
    batches = list(train_batches)
    baseline_loss = None
    start = time.time()

    for step in range(steps):
        batch = batches[step % len(batches)]
        token_batch = target_model.tokenize_batch([ex.text for ex in batch['examples']], batch['max_prompt_len'])
        with torch.no_grad():
            _, attentions, hidden_states = target_model.extract_logits_attentions_hidden(token_batch)
            target_attn = attentions[layer_idx][:, head_idx]
            hidden = hidden_states[layer_idx + 1]

        pred_attn = probe.forward_mechanism_from_hidden(hidden)
        loss = _attention_training_loss(pred_attn, target_attn, mechanism_type)

        if baseline_loss is None:
            bsz, seq_len, _ = target_attn.shape
            tri = torch.tril(torch.ones(seq_len, seq_len, device=target_attn.device))
            tri = tri / tri.sum(dim=-1, keepdim=True).clamp_min(1.0)
            baseline = tri.unsqueeze(0).expand_as(target_attn)
            baseline_loss = float(_attention_training_loss(baseline, target_attn, mechanism_type).item())
            baseline_loss = max(baseline_loss, 1e-8)

        opt.zero_grad()
        loss.backward()
        opt.step()

        if step % 100 == 0 or step == steps - 1:
            elapsed = time.time() - start
            done = step + 1
            eta = (steps - done) * (elapsed / max(done, 1))
            logger.log(f"{run_label}[self-probe-mechanism] step={step:4d}/{steps} | loss={loss.item():.6f} | normed={loss.item()/baseline_loss:.4f} | elapsed={format_seconds(elapsed)} | eta={format_seconds(eta)}")

    return baseline_loss


@torch.no_grad()
def eval_self_probe_mechanism(probe, target_model, val_batches, *, layer_idx: int, head_idx: int, mechanism_type: str, baseline_loss: float):
    probe.eval()
    rows = []
    for batch in val_batches:
        token_batch = target_model.tokenize_batch([ex.text for ex in batch['examples']], batch['max_prompt_len'])
        _, attentions, hidden_states = target_model.extract_logits_attentions_hidden(token_batch)
        target_attn = attentions[layer_idx][:, head_idx]
        hidden = hidden_states[layer_idx + 1]
        pred_attn = probe.forward_mechanism_from_hidden(hidden)
        loss = _attention_training_loss(pred_attn, target_attn, mechanism_type).item()
        top1 = (pred_attn.argmax(dim=-1) == target_attn.argmax(dim=-1)).float().mean().item()
        rows.append({
            'family': batch['family'],
            'source': batch['source'],
            'normed_loss': loss / baseline_loss,
            'top1_match': top1,
        })
    probe.train()
    return rows


def _normalize_token_string(text: str) -> str:
    return ' '.join(text.strip().split()).lower()


def _build_behavior_report_prompt(prompt: str) -> str:
    return (
        'You are reflecting on your own next-token behavior.\n'
        f'Original prompt: {prompt}\n'
        'Question: What single token are you most likely to produce next after the original prompt? '
        'Answer with only that token.'
    )


def _build_behavior_choice_report_prompt(prompt: str, choices: List[str]) -> str:
    numbered = []
    for i, c in enumerate(choices, start=1):
        numbered.append(f"{i}. {c}")
    joined = "\n".join(numbered)
    return (
        'You are reflecting on your own decision process.\n'
        f'Original prompt: {prompt}\n'
        'Question: If you had to answer the original prompt by choosing one of the options below, '
        'which option would you choose? Answer with only the option number.\n'
        f'Options:\n{joined}'
    )


def _build_mechanism_report_prompt(prompt: str, layer_idx: int, head_idx: int) -> str:
    return (
        'You are reflecting on your own attention mechanism.\n'
        f'Original prompt: {prompt}\n'
        f'Question: For the last token of the original prompt, which earlier token position (0-indexed) receives the highest attention in layer {layer_idx} head {head_idx}? '
        'Answer with only an integer.'
    )


@torch.no_grad()
def eval_prompted_self_report_behavior(target_model, examples, *, max_prompt_len: int, max_new_tokens: int, temperature: float):
    rows = []
    for ex in examples:
        if ex.choices:
            target_scores = target_model.score_choices_with_target(ex.text, ex.choices, max_prompt_len)
            target_choice = int(np.argmax(target_scores))
            prompt = _build_behavior_choice_report_prompt(ex.text, ex.choices)
            generated = target_model.generate_texts([prompt], max_prompt_len=max_prompt_len, max_new_tokens=max_new_tokens, temperature=temperature)[0]
            match = re.search(r'-?\d+', generated)
            reported_idx = int(match.group(0)) - 1 if match else None
            parsed_ok = float(reported_idx is not None and 0 <= reported_idx < len(ex.choices))
            reported_choice = ex.choices[reported_idx] if parsed_ok else None
            exact_to_target = float(parsed_ok and reported_idx == target_choice)
            exact_to_gold = float(parsed_ok and ex.answer is not None and _normalize_token_string(reported_choice) == _normalize_token_string(ex.answer))
            rows.append({
                'family': ex.family,
                'source': ex.source,
                'prompt': ex.text,
                'report_task': 'choice',
                'target_choice_index': target_choice,
                'reported_choice_index': reported_idx if reported_idx is not None else -1,
                'target_choice': ex.choices[target_choice],
                'reported_choice': reported_choice or '',
                'gold_answer': ex.answer or '',
                'exact_match': exact_to_target,
                'exact_match_to_target': exact_to_target,
                'exact_match_to_gold': exact_to_gold,
                'parsed_ok': parsed_ok,
            })
            continue

        token_batch = target_model.tokenize_batch([ex.text], max_prompt_len)
        logits, _ = target_model.extract_logits_and_attentions(token_batch)
        attn_mask = token_batch['attention_mask'][0]
        last_pos = int(attn_mask.sum().item()) - 1
        actual_token_id = int(logits[0, last_pos].argmax().item())
        actual_token = _normalize_token_string(target_model.decode_token_id(actual_token_id))

        prompt = _build_behavior_report_prompt(ex.text)
        generated = target_model.generate_texts([prompt], max_prompt_len=max_prompt_len, max_new_tokens=max_new_tokens, temperature=temperature)[0]
        predicted = _normalize_token_string(generated.split()[0] if generated.split() else '')

        rows.append({
            'family': ex.family,
            'source': ex.source,
            'prompt': ex.text,
            'report_task': 'token',
            'actual_token': actual_token,
            'reported_token': predicted,
            'exact_match': float(predicted == actual_token),
            'exact_match_to_target': float(predicted == actual_token),
            'exact_match_to_gold': float('nan'),
            'parsed_ok': float(bool(predicted)),
        })
    return rows


@torch.no_grad()
def eval_prompted_self_report_mechanism(target_model, examples, *, layer_idx: int, head_idx: int, max_prompt_len: int, max_new_tokens: int, temperature: float):
    rows = []
    for ex in examples:
        token_batch = target_model.tokenize_batch([ex.text], max_prompt_len)
        _, attentions = target_model.extract_logits_and_attentions(token_batch)
        target_attn = attentions[layer_idx][0, head_idx]
        attn_mask = token_batch['attention_mask'][0]
        last_pos = int(attn_mask.sum().item()) - 1
        actual_idx = int(target_attn[last_pos].argmax().item())

        prompt = _build_mechanism_report_prompt(ex.text, layer_idx, head_idx)
        generated = target_model.generate_texts([prompt], max_prompt_len=max_prompt_len, max_new_tokens=max_new_tokens, temperature=temperature)[0]
        match = re.search(r'-?\d+', generated)
        reported_idx = int(match.group(0)) if match else None

        rows.append({
            'family': ex.family,
            'source': ex.source,
            'prompt': ex.text,
            'actual_position': actual_idx,
            'reported_position': reported_idx,
            'exact_match': float(reported_idx == actual_idx) if reported_idx is not None else 0.0,
            'within_one': float(abs(reported_idx - actual_idx) <= 1) if reported_idx is not None else 0.0,
            'abs_error': float(abs(reported_idx - actual_idx)) if reported_idx is not None else float('nan'),
            'parsed_ok': float(reported_idx is not None),
            'layer': layer_idx,
            'head': head_idx,
        })
    return rows
